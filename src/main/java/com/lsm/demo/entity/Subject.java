package com.lsm.demo.entity;

public class Subject {
    private String id;
    private String subject;
    private String textbook;
    private String cloudTextbook;
    private String learnRequire;
    private String teaching;
    private String examArrang;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getSubject() {
        return subject;
    }

    public void setSubject(String subject) {
        this.subject = subject;
    }

    public String getTextbook() {
        return textbook;
    }

    public void setTextbook(String textbook) {
        this.textbook = textbook;
    }

    public String getCloudTextbook() {
        return cloudTextbook;
    }

    public void setCloudTextbook(String cloudTextbook) {
        this.cloudTextbook = cloudTextbook;
    }

    public String getLearnRequire() {
        return learnRequire;
    }

    public void setLearnRequire(String learnRequire) {
        this.learnRequire = learnRequire;
    }

    public String getTeaching() {
        return teaching;
    }

    public void setTeaching(String teaching) {
        this.teaching = teaching;
    }

    public String getExamArrang() {
        return examArrang;
    }

    public void setExamArrang(String examArrang) {
        this.examArrang = examArrang;
    }
}
